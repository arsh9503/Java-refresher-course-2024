public class Main { //The name of the public class and the file should be same
    public static void main(String[] args){
        //Assignment Operator --> = --> assigns the right hand operand value to left hand operand. Example a = 5;
        int myInt = 9;
        int newInt = myInt;
        System.out.println(myInt);
        System.out.println(newInt);

//        9 = myInt; It is assignment operator, not the one you use in mathematics
//        left operand should always be a variable
    }
}