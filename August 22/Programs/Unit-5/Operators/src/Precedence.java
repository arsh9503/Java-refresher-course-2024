public class Precedence {
    public static void main(String[] args) {
        //Order of operations
        //BODMAS / PEMDAS
        System.out.println(8-3*3/2); // Multiplication is done first --> Multiplication and division have the same precedence, so the flow will go from left to right
        System.out.println(8/4*2/4);
    }
}
